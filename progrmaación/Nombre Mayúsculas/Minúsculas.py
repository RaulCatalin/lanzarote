# Solicita al usuario que ingrese su nombre completo
nombre_completo = input("Por favor, introduce tu nombre completo: ")

# Muestra el nombre completo en minúsculas
print("Nombre en minúsculas:", nombre_completo.lower())

# Muestra el nombre completo en mayúsculas
print("Nombre en mayúsculas:", nombre_completo.upper())

# Muestra el nombre completo con la primera letra de cada palabra en mayúscula
print("Nombre con capitalización:", nombre_completo.title())
